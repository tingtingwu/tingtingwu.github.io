                    <h4 class="modal-title" id="myMusicLabel">Music: Evaluating UX
                    Design of an iPhone App</h4>
                </div>

                <div class="modal-body">
                    <p><span class="bold">Background:</span> As an avid music
                    listener, I use my iPhone's Music app for a variety of
                    occasions. While I never had qualms with the app in any
                    previous OS, I discovered a design flaw within iOS 7 that
                    really irks me. I, and many others, like to listen to
                    different types (genres) of music depending on present mood
                    or activity. If I'm studying, I might opt for classical
                    versus hip-hop when I'm working out. While the app allows
                    users to shuffle all songs in a playlist (Fig. 1a), by a
                    specific artists (Fig. 1b), or in a certain album (Fig.
                    1c), there is no intuitive way for users to shuffle songs
                    within a genre (Fig. 1d), which is a direct end-goal of
                    mine. Instead, after Googling, I am informed by the Apple
                    Support forum that the work around is either through Siri
                    or through search.</p>

                    <h5>Shuffle Inconsistencies</h5><img class=
                    "center-block img-responsive" src="img/proj/music/m-cs.png"
                    title="Music Shuffle Inconsistencies" alt="Music Shuffle Inconsistencies">

                    <p><span class="bold">Problem:</span> From a heuristics
                    perspective, this is poor design because: 1) it is
                    inconsistent with the rest of the system, 2) it does not
                    minimize user memory load, 3) it lacks documentation to
                    assist users who encounter this issue. One of the key
                    affordances of this app is so users can select the music he
                    wants to listen to. I described how playlists, artists, and
                    albums, all have the shuffle icon as a signifier. However,
                    this icon does not exist for genres. While the signifier
                    appears after searching for a specific genre (in Fig. 2, I
                    searched Alternative), the label is "Cat Stevens" instead
                    of "Alternative" (contrast it to Fig. 1a- d). These
                    inconsistencies increase user confusion, force recall
                    instead of recognition, paired with a lack of help
                    documentation, makes this a poor design decision. I suspect
                    the labeling of the header (e.g. Cat Stevens) after search
                    is actually an oversight. My guess for why the shuffle
                    signifier for genre is hidden is because of Apple's
                    emphasis over visceral/aesthetic design over
                    behavioral/functional. Perhaps the designers did not think
                    there was a good placement for the shuffle icon given the
                    existing layout.</p>

                    <p><span class="bold">Solution:</span> Adding a bar that
                    says "Shuffle All Songs" once a user selects a specific
                    genre would be a very simple solution (Fig. 3). If the user
                    swipes up, the search bar appears. This would be consistent
                    with the rest of the app and reduce user memory load as it
                    eliminates the need to search for a genre to shuffle and
                    also the need for documentation. A usability A/B test where
                    users are timed to see how long it takes them shuffle songs
                    within a genre in the existing system (A) compared to the
                    new implementation (B) can indicate whether this design is
                    an improvement based on task completion rate, time, and
                    numbers of errors committed. If the completion rate, time,
                    and number of errors made are all lower in the new design,
                    then this signifies a design improvement.</p>

                    <p><span class="bold">Note:</span> This was the design at
                    the time of this case study. Apple has since then
                    implemented the solution described.</p>
                </div>

