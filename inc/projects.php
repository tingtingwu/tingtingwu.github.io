                    <h4 class="modal-title" id="myEpicLogueLabel">EpicLogue:
                    Crowdsourcing for Social Impact</h4>
                </div>

                <div class="modal-body">
                <p><span class="bold">Background:</span> Entry for the
                Microsoft Imagine Cup <a href=
                "https://www.imaginecup.com/competition/3012">User Experience
                Challenge</a> under the World Citizenship category.
                Deliverables include: a hero scenario, information architecture
                diagram, two lo-fidelity wireframes, and one hi-fidelity target
                visual image.</p>

                <p><span class="bold">Project:</span> Inspired by Humans of New
                York, Upworthy, and viral videos, we wanted to use
                crowdsourcing to bring attention and opportunity to people down
                on their luck. There is a backstory to everyone's struggle, and
                our idea is for users to piece together those stories and
                identify ways to help. We view each story as an EpicLogue - a
                narrative of how everday users can help someone get back on
                their feet - that utilizes the Internet to make social impact
                more accessible.</p>

                <p><span class="bold">Role:</span> Worked with two other
                students to bring this concept to life. I:</p>

                <ul>
                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Led
                    brainstorming and working sessions throughout the
                    competition</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Generated
                    several hero scenarios (user flow diagrams) based on the
                    initial concept</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Constructed
                    multiple information architecture diagrams and analyzed the
                    trade-offs of each design</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Created lo-fi
                    and hi-fi wireframes using Keynote</li>
                </ul>

