                    <h4 class="modal-title" id="myConveniEatLabel">ConveniEat:
                    Balancing Meal Variety, Cost, and Convenience</h4>
                </div>

                <div class="modal-body">
                <p><span class="bold">Background:</span> As a project for my
                Human-Computer Interaction class, we were tasked with creating
                an interaction design that addresses some type of problem,
                create prototypes, test it with users, and evaluate the
                findings.</p>

                <p><span class="bold">Project:</span> When trying to decide
                what to eat, most people face a trade-off between convenience,
                cost, and variety. People with busy schedules may eat out
                regularly, but accumulate high costs over time. Similarly,
                while cooking is cost effective, individuals who cook for
                themselves often cook a large meal when they have time, and
                then end up eating the same leftovers for several days
                afterwards. ConveniEat is a service that addresses concerns
                around meal variety, cost, and convenience by connecting users
                with one another, allowing them to cook a meal for people on
                one day in exchange for meals on other days.</p>

                <p><span class="bold">Role:</span> Worked with a partner from
                ideation to a final deliverable, I:</p>

                <ul>
                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Brainstormed 50+
                    ideas around food, health, and how computers can solve
                    challenges identified</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Conducted
                    in-depth user interviews to create personas, storyboards,
                    and Work Activity Affinity Diagrams (WAAD)</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Designed
                    interactive prototypes (FluidUI and Keynote) through
                    multiple iterations</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Created
                    user-testing scripts / feedback questionnaire, and
                    conducted usability testing using the think-aloud
                    technique</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Analyzed
                    feedback from Questionnaire for User Interface Satisfaction
                    (QUIS)</li>
                </ul>

