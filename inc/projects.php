                    <h4 class="modal-title" id="myTrickleLabel">Trickle: Raising
                    Awareness One Drink at a Time</h4>
                </div>

                <div class="modal-body">
                <p><span class="bold">Background:</span> According to a 2013
                Cornell PULSE survey, nearly 40% of students binged the past
                two weeks and an undisclosed number do not seek help. Those who
                do seek help from campus health and services are often given a
                paper log to fill out in addition to requiring regular
                check-ins. These logs are cumbersome, can cause stigmatization,
                present recall bias, and do not provide users feedback. We
                wanted to provide students with an anonymous and discreet
                method to monitor their own drinking.</p><img class=
                "center-block img-responsive" src="img/proj/trickle/log.png"
                title="Alcohol Log" alt="Alcohol Log">

                <p><span class="bold">Project:</span> Trickle helps users learn
                about the potential effects alcohol can have in their lives by
                using a real-time tracker to monitor BAC levels, reflecting on
                behaviors from the previous drinking episode, and visualizing
                their personal data. Users also have the ability to set
                personal drinking goals and collect rewards and achievements.
                By becoming aware of the negative effects that are linked to
                specific behaviors, users will be able to more easily avoid
                them and gain greater control over their drinking.</p>

                <p><span class="bold">Role:</span> Worked with a team of two
                other people to conceptualize and design the app, more
                specifically, I:</p>

                <ul>
                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Conducted user
                    research to create personas</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Created
                    questionnaire based on common risk factors and potential to
                    expose trends</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Researched effective
                    data visualizations for surveys outputs</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Designed
                    information architecture and user interface (Android)
                    through multiple iterations</li>

                    <li><span class=
                    "glyphicon glyphicon-chevron-right"></span>Piloted app with
                    select fraternity/sorority members and gathered user
                    feedback</li>
                </ul>

